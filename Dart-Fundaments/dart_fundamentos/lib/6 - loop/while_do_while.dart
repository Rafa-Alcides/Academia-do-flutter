void main() {
  //while
  var numero = 0;
  while (numero <= 10) {
    print(numero);
    numero++;
  }

  // Do while
  // ! mesmo que a condicao for falsa execulta pelo menos uma vez
  var indece = 0;
  while (indece > 0) {
    print(indece);
    indece++;
  }
  do {
    print(indece);
  } while (indece > 0);
}
